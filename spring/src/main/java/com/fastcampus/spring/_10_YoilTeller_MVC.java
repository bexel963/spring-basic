
// - YoilTeller_RemoteCall 컨트롤러를 MVC패턴으로 바꾸기 - Model과 ModelAndView 2가지가 있다.
// - MCV 원리 : _12_MyDispatcherServlet
package com.fastcampus.spring;

import java.io.IOException;
import java.io.PrintWriter;
import java.util.Calendar;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.ExceptionHandler;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.servlet.ModelAndView;

@Controller
public class _10_YoilTeller_MVC {

	@RequestMapping("/getYoilMVC0")							
	public String main(int year, int month, int day, Model model) {
			
		// 0. 유효성 검사
		if(!(isValid(year, month, day)))
			return "yoilError";
		
		// 1. 작업
		char yoil = getYoil(year, month, day);
		
		// 2. 작업 결과를 model에 저장
		model.addAttribute("year", year);
		model.addAttribute("month", month);
		model.addAttribute("day", day);
		model.addAttribute("yoil", yoil);
		
		// 3. 결과를 출력할 view를 리턴
		return "yoil";						// /WEB-INF/views/yoil.jsp
		
	}
	
	@RequestMapping("/getYoilMVC1")							
	public ModelAndView main2(int year, int month, int day) {
			
		ModelAndView mv = new ModelAndView();
		
		// 0. 유효성 검사
		if(!(isValid(year, month, day))) {
			mv.setViewName("yoil");
			return mv;
		}
		
		// 1. 작업
		char yoil = getYoil(year, month, day);
		
		// 2. 작업 결과를 ModelAndView에 저장
		mv.addObject("year", year);
		mv.addObject("month", month);
		mv.addObject("day", day);
		mv.addObject("yoil", yoil);
		
		// 3. 결과를 보여줄 view를 ModelAndView에 지정
		mv.setViewName("yoil");
		
		return mv;	// /WEB-INF/views/yoil.jsp
		
	}

	private boolean isValid(int year, int month, int day) {
		
		return true;
	}

	private char getYoil(int year, int month, int day) {
		
		Calendar cal = Calendar.getInstance();
		cal.set(year, month-1, day);
		
		int dayOfWeek = cal.get(Calendar.DAY_OF_WEEK);	
		return " 일월화수목금토".charAt(dayOfWeek);
	}
	
}
